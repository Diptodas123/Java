class _07_pr07 {
    public static void main(String[] args) {
        
        int i=4,j;  
        do{
            j=1;
            do
            {
                
                System.out.print("* ");
                j++;
            }while(j<=i);
            System.out.print("\n");
            i--;
        }while(i>=1);
        
    }    
}
